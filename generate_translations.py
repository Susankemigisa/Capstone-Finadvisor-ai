def set_nested(data, key_path, value):
    """Set a value in nested dict using dot notation like 'rules.activeRules'"""
    parts = key_path.split('.')
    section = parts[0]
    subkey = parts[1]
    if section not in data:
        data[section] = {}
    data[section][subkey] = value

